#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int number;

	number = n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		number = -n;
	}
	if (number >= 10)
	{
		ft_putnbr_fd(number / 10, fd);
		ft_putnbr_fd(number % 10, fd);
	}
	else
		ft_putchar_fd(number + 48, fd);
}
